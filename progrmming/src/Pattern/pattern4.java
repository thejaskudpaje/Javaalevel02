package Pattern;

public class pattern4 {
public static void main(String[] args) {
	for(int i=1;i<=5;i++) 
	{
		if(i%2==1) 
		{
			for(int j=1;j<=5;j++) 
			{
				System.out.print(j%2+" ");
			}
		}
		else
		{
			for(int k=2;k<=6;k++) 
			{
				System.out.print(k%2+" ");
			}
		}
	System.out.println();
		
	}
}
}
